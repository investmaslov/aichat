"""Клиент к LLM через OpenRouter (OpenAI-совместимый API).

Ключевые идеи:
- системный промпт подставляется первым сообщением автоматически;
- можно указать несколько ключей через запятую — перебираем по очереди,
  на 401/402/429 или сетевой ошибке переходим к следующему;
- если все ключи упали — бросаем LLMError с понятным текстом.

Модуль не зависит от FastAPI, поэтому легко тестируется изолированно.
"""

from __future__ import annotations

import os
import re

import httpx

# Диапазоны emoji и пиктограмм — вырезаем их из ответа, чтобы в UI не было «квадратиков»
# (шрифты интерфейса не содержат emoji-глифов).
_EMOJI_RE = re.compile(
    "[\U0001F000-\U0001FAFF\U00002600-\U000027BF\U0001F1E6-\U0001F1FF"
    "\U00002190-\U000021FF\U00002B00-\U00002BFF\U0000FE00-\U0000FE0F\U0000200D]+"
)


def strip_emoji(text: str) -> str:
    """Убирает emoji/пиктограммы и подчищает двойные пробелы после них."""
    return re.sub(r"[ \t]{2,}", " ", _EMOJI_RE.sub("", text)).strip()

OPENROUTER_URL = "https://openrouter.ai/api/v1/chat/completions"

# Бесплатные модели по умолчанию. Перебираются по очереди: если первая недоступна
# (например, upstream 429 — так бывает у бесплатных моделей), берётся следующая.
DEFAULT_MODELS = [
    "openai/gpt-oss-20b:free",
    "nvidia/nemotron-nano-9b-v2:free",
    "meta-llama/llama-3.3-70b-instruct:free",
]

# Системный промпт — ключевое решение задания (обоснование в COVER_LETTER.md).
# База — логика ТЗ (поверхностный → один вопрос; подробный → благодарность и завершение).
# «Своё» привносим по чуть-чуть: живой комментарий на ответ и игривый тон.
SYSTEM_PROMPT = (
    "Ты — обаятельный исследователь с лёгким чувством юмора. Ведёшь короткое интервью про "
    "то, как человек выбирал свой последний онлайн-курс.\n\n"
    "На ответ респондента реагируй так:\n"
    "1) Сначала — короткий тёплый КОММЕНТАРИЙ на сказанное, с лёгким приколом, по-доброму "
    "(одна фраза, покажи, что услышал именно его).\n"
    "2) Затем ОЦЕНИ глубину ответа и действуй строго по правилу:\n"
    "   • Ответ ПОВЕРХНОСТНЫЙ (нет конкретики: не названы критерии выбора, источники "
    "информации, сравнение вариантов, цена, отзывы или личный опыт) — задай РОВНО ОДИН "
    "короткий уточняющий вопрос, который поможет раскрыть мотивацию или критерии.\n"
    "   • Ответ ПОДРОБНЫЙ (раскрыто хотя бы 2–3 таких аспекта) — искренне поблагодари и "
    "заверши интервью. Новый вопрос НЕ задавай.\n\n"
    "Правила:\n"
    "- Если вопрос нужен — ровно один, и только один знак «?» во всём ответе. В завершающем "
    "сообщении знака «?» быть не должно.\n"
    "- Пиши СТРОГО на русском (названия брендов можно). Без латинских слов и посторонних символов.\n"
    "- НЕ используй эмодзи и смайлики — только слова и знаки препинания.\n"
    "- Дружелюбно, игриво и кратко: 2–4 предложения. Только про онлайн-курсы."
)

# HTTP-коды, при которых имеет смысл попробовать другой ключ или другую модель:
# 401/402 — проблема ключа; 429 — лимит; 404 — модель недоступна на этом ключе.
_RETRYABLE_STATUS = {401, 402, 404, 429}

# После скольких ответов респондента интервью принудительно завершается благодарностью.
CLOSE_AFTER = 4


class LLMError(Exception):
    """Не удалось получить ответ ни от одного ключа."""


def get_keys() -> list[str]:
    """Разбирает OPENROUTER_KEYS (через запятую), чистит пробелы и пустые значения."""
    raw = os.getenv("OPENROUTER_KEYS", "")
    return [key.strip() for key in raw.split(",") if key.strip()]


def get_models() -> list[str]:
    """Разбирает MODEL (одна модель или несколько через запятую). По умолчанию — DEFAULT_MODELS."""
    raw = os.getenv("MODEL", "")
    models = [m.strip() for m in raw.split(",") if m.strip()]
    return models or list(DEFAULT_MODELS)


async def chat(messages: list[dict]) -> str:
    """Отправляет диалог в LLM и возвращает текст ответа.

    messages — история вида [{"role": "user"|"assistant", "content": "..."}].
    Системный промпт добавляется автоматически первым сообщением.

    Перебор двухуровневый: по моделям, и внутри каждой — по ключам. На 401/402/404/429
    или сетевой ошибке переходим к следующей комбинации; возвращаем первый успех.
    """
    keys = get_keys()
    if not keys:
        raise LLMError(
            "Не задан ни один ключ OpenRouter. Укажите OPENROUTER_KEYS в .env "
            "(получить можно бесплатно на openrouter.ai)."
        )

    chat_messages = [{"role": "system", "content": SYSTEM_PROMPT}, *messages]

    # Детерминированное завершение: после CLOSE_AFTER ответов респондента подставляем
    # директиву «пора закругляться». Модель на игривом промпте сама тянет интервью,
    # поэтому финал гарантируем на бэкенде — так надёжно проявляется ветка ТЗ
    # «подробный ответ → поблагодари и заверши».
    if sum(1 for m in messages if m.get("role") == "user") >= CLOSE_AFTER:
        chat_messages.append({
            "role": "system",
            "content": (
                "Интервью пора завершать. Тепло и с лёгким приколом поблагодари за подробные "
                "ответы и заверши беседу. НОВЫЙ ВОПРОС НЕ ЗАДАВАЙ, знака „?“ в ответе быть не должно."
            ),
        })

    last_error = "неизвестная ошибка"

    async with httpx.AsyncClient(timeout=60.0) as client:
        for model in get_models():
            for key in keys:
                try:
                    response = await client.post(
                        OPENROUTER_URL,
                        headers={"Authorization": f"Bearer {key}"},
                        json={
                            "model": model,
                            "messages": chat_messages,
                            # Умеренная температура: у маленьких бесплатных моделей меньше
                            # «улёта» в другие языки, но игривость сохраняется.
                            "temperature": 0.7,
                            # Запас токенов, чтобы у reasoning-моделей хватало места на сам
                            # ответ и content не приходил пустым (обрезанным).
                            "max_tokens": 1000,
                        },
                    )
                except httpx.HTTPError as exc:
                    last_error = f"{model}: сетевая ошибка: {exc}"
                    continue

                if response.status_code in _RETRYABLE_STATUS:
                    last_error = f"{model}: HTTP {response.status_code}"
                    continue

                if response.status_code >= 400:
                    # Не ретраим прочие ошибки — они не про ключ/модель, а про сам запрос.
                    raise LLMError(f"LLM вернула ошибку HTTP {response.status_code}: {response.text}")

                data = response.json()
                # content бывает None/пустым у reasoning-моделей (ответ ушёл в reasoning или
                # обрезался). Тогда не падаем, а переходим к следующей модели/ключу.
                content = strip_emoji((data["choices"][0]["message"].get("content") or ""))
                if not content:
                    last_error = f"{model}: пустой ответ модели"
                    continue
                return content

    raise LLMError(
        f"Ни одна бесплатная модель не ответила (перебрали все модели и ключи). "
        f"Последняя причина: {last_error}. Попробуйте ещё раз через несколько секунд."
    )
